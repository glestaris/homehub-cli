package cmd

import (
	"testing"

	gomock "github.com/golang/mock/gomock"
	"github.com/jamesnetherton/homehub-cli/service"
)

func TestDhcpAuthoritativeCommand(t *testing.T) {
	ctrl := gomock.NewController(t)
	defer ctrl.Finish()

	hub := NewMockHub(ctrl)
	service.SetHub(hub)
	service.AuthenticationComplete()

	hub.EXPECT().DhcpAuthoritative().Return(true, nil)

	AssertCommandOutput(t, NewDhcpAuthoritativeCommand(NewLoginCommand()))
}
