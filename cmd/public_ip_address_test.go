package cmd

import (
	"testing"

	gomock "github.com/golang/mock/gomock"
	"github.com/jamesnetherton/homehub-cli/service"
)

func TestPublicIPAddressCommand(t *testing.T) {
	ctrl := gomock.NewController(t)
	defer ctrl.Finish()

	hub := NewMockHub(ctrl)
	service.SetHub(hub)
	service.AuthenticationComplete()

	hub.EXPECT().PublicIPAddress().Return("11.22.33.44", nil)

	AssertCommandOutput(t, NewPublicIPAddressCommand(NewLoginCommand()))
}
