package cmd

import (
	"testing"

	gomock "github.com/golang/mock/gomock"
	"github.com/jamesnetherton/homehub-cli/service"
)

func TestLocalTimeCommand(t *testing.T) {
	ctrl := gomock.NewController(t)
	defer ctrl.Finish()

	hub := NewMockHub(ctrl)
	service.SetHub(hub)
	service.AuthenticationComplete()

	hub.EXPECT().LocalTime().Return("2018-01-01 00:00:00", nil)

	AssertCommandOutput(t, NewLocalTimeCommand(NewLoginCommand()))
}
