package cmd

import (
	"testing"

	gomock "github.com/golang/mock/gomock"
	"github.com/jamesnetherton/homehub-cli/service"
)

func TestMaintenanceFirmwareVersionCommand(t *testing.T) {
	ctrl := gomock.NewController(t)
	defer ctrl.Finish()

	hub := NewMockHub(ctrl)
	service.SetHub(hub)
	service.AuthenticationComplete()

	hub.EXPECT().MaintenanceFirmwareVersion().Return("Test firmware version", nil)

	AssertCommandOutput(t, NewMaintenanceFirmwareVersionCommand(NewLoginCommand()))
}
